const questions = [];

for (const lesson of lessons) {
  const lessonQuestions = quizData[lesson.id];
  if (!lessonQuestions) {
    console.error(`Missing quiz data for lesson: ${lesson.id} (${lesson.topic})`);
    process.exit(1);
  }
  if (lessonQuestions.length !== 8) {
    console.error(`Expected 8 questions for lesson ${lesson.topic}, got ${lessonQuestions.length}`);
    process.exit(1);
  }

  for (const q of lessonQuestions) {
    if (!q.opts.includes(q.ans)) {
      console.error(`Correct answer "${q.ans}" not found in options for question: ${q.q}`);
      process.exit(1);
    }

    questions.push({
      id: randomUUID(),
      lesson_id: lesson.id,
      question_type: "multiple_choice",
      question_text: q.q,
      options: q.opts,
      correct_answer: q.ans,
      explanation: q.exp,
    });
  }
}

// Validate totals
console.log(`Total lessons: ${lessons.length}`);
console.log(`Total questions: ${questions.length}`);
console.log(`Expected: ${lessons.length * 8}`);

if (questions.length !== 208) {
  console.error(`ERROR: Expected 208 questions, got ${questions.length}`);
  process.exit(1);
}

// Write output
const outPath = join(__dir, 'quiz_questions.json');
writeFileSync(outPath, JSON.stringify(questions, null, 2), 'utf-8');
console.log(`Wrote ${outPath}`);
